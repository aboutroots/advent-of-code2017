def count_sums1(captcha):
    digits = list(captcha)
    pair_sum = 0
    for i in range(len(digits)):
        j = i + 1
        if j == len(digits):
            j = 0

        if digits[i] is digits[j]:
            pair_sum += int(digits[i])

    return pair_sum


def count_sums2(captcha):
    digits = list(captcha)
    pair_sum = 0
    halfway = len(digits)//2
    for i in range(len(digits)):
        j = i - halfway

        if digits[i] is digits[j]:
            pair_sum += int(digits[i])

    return pair_sum

